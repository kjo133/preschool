          <div class="temp" topic="eligibility">
            <h3>Who is eligible to attend?</h3>
            <p>Emmanuel Lutheran preschool welcomes students from all racial, ethnic, and religious backgrounds. We work to cultivate the individuality of each child and to encourage independence and creativity.  We believe that every child is a unique child of God made by him in love.  At the same time, we are teaching students to cooperate with one another and to respect and help others.  We believe our program enables our students to meet challenges with confidence and success.</p>
          </div>


          <div class="temp" topic="developmental">
            <h3>Developmental Learning</h3>
    		<p>Play is vital to growing children and a major portion of time will be devoted to activity and learning centers (art, dramatic play, reading, math, manipulatives, science, music, etc.) that permit and encourage experimentation and exploration. Our program includes activities which include active and quiet child-selected and teacher-directed activities.</p>
            <p>To understand more about our developmental learning philosophy you can click on this link, <a href="http://www.cnn.com/2010/OPINION/12/29/christakis.play.children.learning/index.html">Let them Play</a>, to read a CNN Opinion article on the subject.  </p>
