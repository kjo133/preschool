            <h3>Class Size Policy</h3>
            <ul>
              <li>2’s classes - up to 12 students w/three adults – a teacher, assistant, and aide.</li>
              <li>3’s classes - up to 12 students w/two adults – a teacher and an assistant.</li>
              <li>4’s classes - up to 12 students w/two adults – a teacher and an assistant.</li>
              <li>Or - up to 18 students (much larger room) w/up to three adults, a teacher, assistant, and floating staff member.</li>
              <li>Our staff floater is available to all classrooms throughout the day as needed.</li>
            </ul>
