


    <h2>About</h2>
    
    <div class="col-md-4">
		  <p>Emmanuel Lutheran Preschool has been serving our community for over 50 years.</p>
        <p>We are licensed by the Commonwealth of Virginia.</p>
        <p>We accept children beginning at 2-1/2 through 5 years old (Pre-K).  We strive to work with parents to create a Christian environment where we meet the emotional, social, physical, intellectual, and spiritual needs of each child.</p>
    </div>
    
    <div class="col-md-4">  
		  <p>We offer a developmentally appropriate program, providing rich, hands-on experiences that allow each child to become successful learners.</p>
    	<img class="visible-md visible-lg imageBorder" src="images/girlBoySpelling.jpg" />
      <p>Our goal for each student is to instill a love of God, love of learning, and love of self and others.</p>
    </div>
    
    
